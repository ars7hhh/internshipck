friend_a={"Python", "Cooking", "Hiking", "Movies"}
friend_b = {"Hiking", "Gaming", "Photography", "Python"}
common_interests=friend_a.intersection (friend_b)
print("results of intersection:",common_interests)
all_interests=friend_a.union (friend_b)
print("results of union:",all_interests)
differences=friend_a.difference (friend_b)
print("results of differences:",differences)

